/* Operdortes de Comparação
&& (AND) - Todas as expressões precisam ser verdadeiras para retornar true
|| (OR) - Apenas uma das expressões precisa ser verdadeira para retornar true
! (NOT) - Nega uma expressão, ou seja, transforma true em false e vice-versa

*/
const expressaoAnd = true && true && true; //true
console.log(expressaoAnd);

const expressaoOr = true || false; //true
console.log(expressaoOr);

const expressaoNot = !true; //false
console.log(expressaoNot);